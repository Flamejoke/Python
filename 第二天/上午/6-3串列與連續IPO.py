# 匯入 sys 模組（這個模組能讓程式讀取使用者輸入的內容）
import sys

# 程式開始執行
#start
# 迴圈：每次讀取使用者輸入的「一行文字」（s 就是這行文字）
for s in sys.stdin:
    # 把這行文字按「空格」拆分，變成字串列表（比如輸入 10 20，就拆成 ['10', '20']）
    nums = s.split()

    # 計算列表裡有幾個元素（比如 ['10','20'] 就有 2 個，size=2）
    size = len(nums)
    # 迴圈：把列表裡的每個「字串數字」轉成真正的「整數」（比如 '10' → 10）
    for i in range(size):
        nums[i] = int(nums[i])# 6. sum(nums)：計算列表裡所有整數的總和（比如 80+90+70=240）

    # 計算列表裡所有數字的總和，再除以元素個數（求平均值），最後列印出來
    print(sum(nums) / size)
